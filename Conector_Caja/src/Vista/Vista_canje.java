    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        UserController uc = new UserController();
         
        FuncionarioController fc = new FuncionarioController();
        Usuario usuario;
        
        try {
            
            if (uc.validarUsuario(nombre_txt.getText(), pass_txt.getText())) {
                mensaje_autorizado.setText("Usuario autenticado");
                caja = new Caja();
                caja.setVisible(true);
                
                usuario = uc.retornaUsuario(nombre_txt.getText(), pass_txt.getText());
               Funcionario funcionario = fc.retornarFuncionarioID(usuario.getFuncionario_Id_Funcionario());
                caja.nombreCajero.setText(funcionario.getNombre());
               
                this.dispose();
                
            }else{
                mensaje_autorizado.setText("Usuario no valido");
            }
            
            
            
            
        } catch (NoSuchAlgorithmException | UnsupportedEncodingException | ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Vista_canje.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

